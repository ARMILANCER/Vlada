# -> Assignment(operation = elaboration) -> output print(f"Division: {operation}")
operation = value1 - value2;
print(f"Subtraction: {operation}")
operation = value1 + value2;
print(f"Sum: {operation}")
operation = operation = value1 * value2;
print(f"Multiplication: {operation}")
operation = operation = value1 / value2;
print(f"Division: {operation}")

